export enum DiscordTimestampFormat {
  ShortTime = 't',       // 9:41 PM
  LongTime = 'T',        // 9:41:30 PM
  ShortDate = 'd',       // 30/06/2025
  LongDate = 'D',        // 30 June 2025
  ShortDateTime = 'f',   // 30 June 2025 9:41 PM
  LongDateTime = 'F',    // Monday, 30 June 2025 9:41 PM
  Relative = 'R',        // in 2 minutes
}